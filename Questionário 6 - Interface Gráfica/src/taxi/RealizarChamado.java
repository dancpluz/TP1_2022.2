    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if (ftxtData.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "A data deve ser informada!", "Aviso", JOptionPane.ERROR_MESSAGE);
        }else{
            Chamado cha;
            String datai = ftxtData.getText();
            Cliente cliente = new Cliente();
            String id = "",data = "",origem = "",destino = "";

            for (int i=0;i<listaChamados.size();i++) {
                cha = listaChamados.get(i);

                if (datai.equals(cha.getData())){
                    id = String.valueOf(cha.getId());
                    data = cha.getData();
                    origem = cha.getOrigem();
                    destino = cha.getDestino();
                    cliente = cha.getCliente();
                }
            }

            if (data.equals("")) {
                JOptionPane.showMessageDialog(null, "Não ocorreu nenhum chamado nessa data.", "Aviso", JOptionPane.ERROR_MESSAGE);
                txtID.setText(id);
                ftxtData.setText(data);
                txtOrigem.setText(origem);
                txtDestino.setText(destino);
                
            }else{
                txtID.setText(id);
                ftxtData.setText(data);
                txtOrigem.setText(origem);
                txtDestino.setText(destino);
            }

            ftxtData.selectAll();
            ftxtData.requestFocus();
            btnNovo.setEnabled(false);
            btnCancelar.setEnabled(true);
            btnSalvar.setEnabled(false);
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnPesquisar.setEnabled(true);
            btnOK.setEnabled(true);
            formatarCampo();
        }
    }//GEN-LAST:event_btnOKActionPerformed

