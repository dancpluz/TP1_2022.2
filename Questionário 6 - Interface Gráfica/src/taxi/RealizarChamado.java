    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        int index = comCliente.getSelectedIndex();
                
        if (txtID.getText().equals("") || txtDestino.getText().equals("") || txtOrigem.getText().equals("") || ftxtData.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha os campos", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else if (index==0) {
            JOptionPane.showMessageDialog(null, "Você deve selecionar um cliente", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else if (ftxtData.getText().trim().length()<10){
            JOptionPane.showMessageDialog(null, "A data deve estar no formato dd\\mm\\aaaa", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {

            int id = Integer.parseInt(txtID.getText());
            String data = ftxtData.getText();
            String origem = txtOrigem.getText();
            String destino = txtDestino.getText();

            if (botao.equals("novo")) {

                Chamado chamado = new Chamado(id,data,origem,destino);

                chamado.setCliente(CadastroCliente.listaClientes.get(index-1));
                
                listaChamados.add(chamado);

                CadastroCliente.listaClientes.get(index-1).criarChamado(chamado);
                
                JOptionPane.showMessageDialog(null, "Chamado realizado com sucesso!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            } else if (botao.equals("editar")) {
                int i = tabChamados.getSelectedRow();

                listaChamados.get(i).setId(id);
                listaChamados.get(i).setData(data);
                listaChamados.get(i).setOrigem(origem);
                listaChamados.get(i).setDestino(destino);
                listaChamados.get(i).setCliente(CadastroCliente.listaClientes.get(index-1));
            }

            carregarTabelaChamados();

            btnNovo.setEnabled(true);
            btnCancelar.setEnabled(true);
            btnSalvar.setEnabled(true);
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnPesquisar.setEnabled(true);
            btnOK.setEnabled(false);
            formatarCampo();

            txtID.setEnabled(false);
            txtDestino.setEnabled(false);
            txtOrigem.setEnabled(false);
            ftxtData.setEnabled(false);
            comCliente.setEnabled(false);

            txtID.setText("");
            txtDestino.setText("");
            txtOrigem.setText("");
            ftxtData.setText("");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

